// Helper function to get icon component by name
export function getIconComponent(iconName: string, className?: string) {
  const icons = {
    sql: SqlIcon,
    error: ErrorIcon,
    log: LogIcon,
    http: HttpIcon,
    data: DataIcon,
    clock: ClockIcon,
    hash: HashIcon,
    check: CheckIcon,
    cpu: CpuIcon,
    database: DatabaseIcon
  }

  const IconComponent = icons[iconName as keyof typeof icons] || DataIcon
  return <IconComponent className={className} />
}
